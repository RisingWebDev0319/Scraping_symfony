    /**
     * @param GetFreelancerDetailsMessage $message
     */
    public function __invoke(GetFreelancerDetailsMessage $message)
    {
        try {
            // TODO Fetch Details from Freelancer and parse them
            $freelancerDetails = $this->client->getClient()->request('GET', $message->getHref());
            $content = json_decode($freelancerDetails->getContent());

            $freelancer = [
                'userId' => $content->expert->id,
                'image' => $content->expert->profilePictureUrl,
                "title" => $content->profile->coreCompetence,
                "availability" => [
                    "from" => $content->profile->availableFrom
                ],
                "price" => [
                    "type" => "hourly",
                    "amount" => $content->profile->expectedPayment->rate,
                ],
                "countries" => $this->code_to_country($content->profile->regions),
                "areas" => [
                    "zipcodes" => $content->profile->regions,
                    "surroundings" => $content->profile->locations
                ],
                "projects" => $content->profile->projects,
                "languages" => $content->profile->foreignLanguages,
                "tags" => $content->profile->allCompetencesWithSynonyms
            ];

            if($message->getId()) {
                file_put_contents("var/profiles/" . $message->getId() . ".json", json_encode($freelancer));
            }

        } catch (\Exception $e) {
            $this->logger->error(sprintf("Error parsing Freelancer: %s (Code: %s, Message: %s)", $message->getHref(), $e->getCode(), $e->getMessage()), $e->getTrace());
        }
    }
